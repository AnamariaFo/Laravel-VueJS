     */
    public function update($id)
    {
        $existingGame = Game::find($id);

        if($existingGame) {
            $existingGame->added_to_cart = !$existingGame->added_to_cart;
            $existingGame->save();

            return new GameResource($existingGame);
        }
        return response()->json(['error' => 'Game not found'], 403);;
    }

